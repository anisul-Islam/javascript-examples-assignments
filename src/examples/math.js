// check these methods from Math objects

console.log(Math.max(25,23));
console.log(Math.min(25,23));
console.log(Math.sqrt(25));
console.log(Math.pow(2,3)); // 2 ** 3 (recommended)
console.log(Math.abs(-2)); 
console.log(Math.sin(45)); 
console.log(Math.floor(2.4)); 
console.log(Math.ceil(2.4)); 
console.log(Math.round(2.5)); 

// value always between 0-1 (fraction values); 
console.log(Math.random()); 

// value now between 0-5 (but not 5; less than 5; fraction values)
console.log(Math.random() * 5); 

// value now between 0-5 (but not 5; less than 5; no fraction values)
console.log(Math.floor(Math.random() * 5)); 


// value now between 1-5  (no fraction values)
console.log(Math.floor(Math.random() * 5) + 1); 

